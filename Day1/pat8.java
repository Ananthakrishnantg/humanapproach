import java.io.*;
import java.util.*;
class pat8
{
	public static void main(String[] s)
	{
		int n=5;
		for( int i=1;i<=n;i++)
		{
			for(int k=1;k<=i;k++)
			{
				System.out.print(" ");
			}
			for(int j=1;j<=5;j++)
			{
				System.out.print("*");
			}
		System.out.println();
		}
	}
}
			